package coe528.lab1;

public abstract class Passenger {
	private String name;
	private int age;
	public Passenger(String name, int age) {
		this.setName(name);
		this.setAge(age);
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public abstract double applyDiscount(double p);
}